            <el-cascader
            clearable
            v-model="selected"
            :options="options"
            :props="defaultProps"
            @change="handleChange">
          </el-cascader>
          </el-form-item>
          
        </el-tab-pane>
        <el-tab-pane name="2" label="商品参数">
          <el-form-item :label="item1.attr_name" v-for="(item1,i) in manyArr" :key="i">
            <el-checkbox-group v-model="item1.attr_vals">
              <el-checkbox border :label="item2" v-for="(item2,i) in item1.attr_vals" :key="i"></el-checkbox>
            </el-checkbox-group>
          </el-form-item>
