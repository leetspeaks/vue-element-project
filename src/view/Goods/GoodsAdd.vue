<script>
import 'quill/dist/quill.core.css'
import 'quill/dist/quill.snow.css'
import 'quill/dist/quill.bubble.css'

import {quillEditor} from 'vue-quill-editor'
export default {
  components: {
    quillEditor
  },
  data() {
    return {
      active: '1',
      form: {
        goods_name: '',
        goods_price: '',
        goods_number: '',
        goods_weight: '',
        goods_introduce: '',
        goods_cat: '',
        pics: [],
        attrs: []
      },
      // 级连绑定的数据
      selected: [],
      options: [],
      defaultProps: {
          label: 'cat_name',
          value: 'cat_id',
          children: 'children'
      },
      // 动态数据
      manyArr: [],
      // 静态数据
      onlyArr: [],
      headers:{
        Authorization: localStorage.getItem('token')
      }
    }
  },
  created() {
      this.category()
  },
  methods: {
    // 点击添加商品发送请求
    async addGoods(){
      let arr1 = this.manyArr.map((item)=>{
        return {attr_id: item.attr_id,attr_vals: item.attr_vals}
      })
      let arr2 = this.onlyArr.map((item)=>{
        return {attr_id: item.attr_id,attr_vals: item.attr_vals}
      })
      let arr = [...arr1,...arr2]
      this.form.goods_cat = this.selected.join(',')
      const res = await this.$http.post('goods',this.form)
      console.log(res)
      this.$router.push({name: 'goods'})
    },
    handlePreview(file){
      console.log(file)
    },
    handleRemove(file){
      console.log('移除')
      console.log(file)
      let Index = this.form.pics.findIndex((item)=>{
        return item.pic === file.response.data.tmp_path
      })
      this.form.pics.splice(Index,1)
    },
    handleSuccess(file){
      console.log('成功')
      console.log(file)
      this.form.pics.push({
        pics: file.data.tmp_path
      })
    },
    async tabChange(){
      if(this.active === '2'){
        if(this.selected.length !== 3){
          this.$message.warning('请选择三级分类')
          return
        }
        const res = await this.$http.get(`categories/${this.selected[2]}/attributes?sel=many`)
        // console.log(res)
        this.manyArr = res.data.data
        this.manyArr.forEach(item => {
          item.attr_vals = item.attr_vals.length === 0 ? [] : item.attr_vals.trim().split(',')
        });
      }else if(this.active === '3'){
        if(this.selected.length !== 3){
          thisthis.$message.warning('请选择三级分类')
          return
        }
        const res = await this.$http.get(`categories/${this.selected[2]}/attributes?sel=only`)
        console.log(res)
        this.onlyArr = res.data.data
      }
    },
      async category(){
          const res = await this.$http.get('categories?type=3')
          // console.log(res)
          this.options = res.data.data
      },
      handleChange(){}
  },
}
